                        <h3>What Is The Maximum Annual Allowable Rent Increase Under State Of California Rent Control?</h3>
                        <p>For units covered by the rent-ceiling limitations of State of California rent control, a landlord may only increase the rent each year by 5% plus the annual percent change in the Consumer Price Index, up to a maximum of 10%. If the landlord chooses to do smaller increases, the landlord can only increase the rent two times in one year. The law is retroactive to March 15, 2019. If a landlord has raised the rent above the allowable amount from March 15, 2019 to January 1, 2020, the rent will be rolled back, but the landlord shall not be liable to the tenant for any rent overpayments.</p>
                        <h3>Can A Master Tenant Rent Gouge Under State Of California Rent Control?</h3>
                        <p>Under State of California rent control, a master tenant cannot charge subtenants more than the master tenant is paying to the landlord.</p>
                        <h3>Does State Rent Control Apply To Single-Family Homes And Condominiums In Rent-Controlled Jurisdictions?</h3>
                        <p>In cities and counties with rent control, single-family homes and condominiums are covered by state-law rent caps where the units are not otherwise covered by local rent caps, as long as the units are not otherwise exempt under state law. For example, the San Francisco Rent Ordinance does not have rent caps for single-family homes and condominiums for post-1996 tenancies. As long as the single-family home or condominium is built more than fifteen years ago (and the tenancy and unit meet all other state rent control requirements, state rent control would apply to limit rent increases.</p>
                        <h3>If I Am In A City With Existing Rent-Control, Does State Or Local Just-Cause Eviction Protection Apply?</h3>
                        <p>If you had just cause eviction protection for your tenancy before California state rent control was enacted, your local ordinance will apply to your tenancy, even if the state law rent control offers more protection. However, if your city or county enacts a new ordinance with just-cause eviction protection after September 1, 2019, the local ordinance will only apply if it offers stronger protections than the state law.</p>
                        <h3>If I Am In A City With Existing Rent-Control, Do State Or Local Rent Caps Apply?</h3>
                        <p>If your tenancy is subject to rent caps under an existing local rent-control ordinance, the local ordinance will apply if it is a more restrictive that state rent control. For example, if your local rent-control ordinance allows a 10% annual increase and the state rent control statute allows 8%, the state rent control statute will apply.</p>
                        <h3>Can My Landlord Bank Rent Increases Under The State Of California Rent Control?</h3>
                        <p>Under State of California rent control, if a landlord does not take a rent increase in any given year, the landlord cannot bank the prior rent increase.</p>


