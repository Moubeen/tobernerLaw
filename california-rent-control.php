                      <h3>Step One: Is My Unit The Type That Is Covered By State Of California Eviction Protection?</h3>
                      <p>All units in California that have certificates of occupancy that are fifteen years or older have eviction protection, except the following:<br><br>
                      • Single-family homes and condominiums owned by an individual and not a corporation, an LLC owned by a corporation, or a real estate investment trust, but only if the owner of the single-family home or condominium discloses in the lease that the unit is exempt.<br>
                      • Units where one tenant lives with a landlord and shares a bathroom or kitchen with the landlord.<br>
                      • Single-family homes with in-law units, accessory-dwelling units, or granny units, where the landlord occupies the single-family home and rents out no more than two units or bedrooms.<br>
                      • Duplexes where the landlord has continuously occupied one of the units since the beginning of the tenancy as the owner’s principal place of residency.<br>
                      • Dorms owned and operated by a school.<br>• Hotels rooms where maid service, mail or other room services are provided.<br>
                      • Housing accommodations in a nonprofit hospital, religious facility, extended care facility, or licensed elder care or adult care facility.<br>
                       <br>• Affordable housing units, BMR units, and Section 8 units, including Section 8 voucher units.<br>
                       • Units covered by local rent control, even if the local ordinance has weaker eviction protection.<br>• Mobile homes</p>
                       <h3>Step Two: Is My Specific Tenancy Covered By State Of California Just-Cause Eviction Protection?</h3>
                       <p>A tenant or group of tenants who have continuously and lawfully occupied a unit for twelve months from the beginning of the tenancy have eviction protection. However, if another adult tenant is added to the unit after the beginning of the lease, the household does not have eviction protection until either (1) all adult tenants have continuously and lawfully resided in the unit for twelve or more months, or (2) at least one adult tenant in the unit has continuously and lawfully resided in the unit for twenty-four months.</p>
                       <h3>Am I Entitled To Relocation Payments When I Am Evicted Under State Of California Rent Control?</h3>
                       <p>A landlord who evicts a tenant for an owner move in, a relative move in, demolition, substantial remodel, pursuant to city or county abatement, or to withdraw the unit from the rental market, must provide one of the following forms of tenant assistance at the landlord’s discretion:<br><br>
                       1 .Provide one month’s rent within fifteen days of serving the eviction notice; or<br>
                       2. Waive the final month’s rent.<br><br>The eviction notice must inform the tenant of the right to relocation assistance. If the tenant does not vacate at the expiration of the notice, the landlord may recover the relocation assistance as damages. A landlord is entitled to credit the relocation against relocation required by a different statute or ordinance.</p>
                       <h3>Can I Waive My Rights To The Protections Under State Of California Rent Control?</h3>
                       <p>A tenant cannot waive their rights under the Tenant Protection Act of 2019. Any waiver of rights by a tenant is void against public policy.</p>
                       <h3>Does My Lease Have To Say I Am Protected Under State Of California Rent Control?</h3>
                       <p>Any lease signed or renewed after July 1, 2020 must include the below disclosure. By August 1, 2020, a landlord must disclose the below in writing to existing leaseholders. This disclosure requirement does not apply to tenants already covered by more stringent city or county rent control. Nor does it apply to units that will never be covered by eviction protection or rent-ceiling protection.<br><br>
                       “California law limits the amount your rent can be increased. See Section 1947.12 of the Civil Code for more information. California law also provides that after all 
                        of the tenants have continuously and lawfully occupied the property for 12 months or more or at least one of the tenants has continuously and lawfully 
                        occupied the property for 24 months or more, a landlord must provide a statement of cause in any notice to terminate a tenancy. See Section 1946.2 of the Civil 
                        Code for more information.”</p><br><br>
                        <h2>Am I Covered By The Rent-Ceiling Limitations Of The State Of California Rent Control Statute?</h2>
                        <p>All units in California are covered by the rent-ceiling limitations of State of California rent control except:<br><br>
                        • Affordable housing units, BMR units, project-based subsidized housing and Section 8 voucher units<br>
                        • Dorms constructed and maintained by a university and occupied by enrolled students<br>
                        • Units covered by a local rent ordinance that has stronger rent-increase limits<br>
                        • Buildings that were built less than fifteen years ago<br>
                        • Condominiums and single-family homes, unless the owner is a real estate investment trust, a corporation, or an LLC in which at least one member is a <br>
                        corporation. Condominiums and single-family homes that are individually owned are only exempt from the rent-ceiling limitations if the owner discloses in the lease that the unit is not subject to the rent limits. (For tenancies existing before July 1, 2020, the disclosure of exempt status need not be stated in the lease but must be sent in writing.)<br>
                        • Duplexes where the landlord has continuously occupied one of the units since the beginning of the tenancy<br>
                        • Mobile homes</p>
