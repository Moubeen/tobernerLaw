                      <h2>Do I Have Eviction Protection Under California’s Tenant Protection Act?</h2>
                      <p>Determining whether you have just-cause eviction protection is a two-step analysis. First, you must determine whether your unit qualifies. Second, you must determine whether you have met the length-of-tenancy requirement. Below, we explain how to determine whether you have eviction protection.</p>
