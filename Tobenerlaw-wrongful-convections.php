                <div class="col-lg-6">
                    <p>A landlord and anyone who assists a landlord in attempts to wrongfully evict can be held liable for wrongful eviction. Real estate agents, property managers, and relatives are often held liable for wrongful eviction.<br><br>
                    The damages awarded for wrongful eviction include (1) the lost rental value of the rent-controlled unit, (2) moving costs and statutory relocation fees, and (3) emotional distress. Under the Oakland Rent Ordinance, Berkeley Rent Ordinance, and San Francisco Rent Ordinance, the damages are tripled.<br><br>
                    The lost rental value is calculated as the fair market monthly rental value of the lost unit less the actual rent paid by the tenant when evicted times the number of years the tenant expected to stay in the unit. Chacon v. Litke, 181 Cal. App. 4th 1234 (2010) (upholding an award of three times, twenty years of projected lost rental value).</p>
                </div>
                <div class="col-lg-6">
                    <p>For example, where a tenant would have remained in a unit for another ten years and was paying $1,000 below market rate, the value of the lost unit is $120,000. This is then tripled. Attorney fees and costs are also recoverable.<br><br>
                    The lost rental value, moving costs and statutory relocation fees are automatically tripled. Emotional distress damages are tripled where a landlord acted in knowing or reckless disregard of the eviction protections under the ordinance.</p>
