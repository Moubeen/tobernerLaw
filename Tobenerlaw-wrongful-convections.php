                    <h3>Southern California</h3>
                          <h5>West Hollywood</h5>
                          <p>In West Hollywood, a landlord or any agent of the landlord can be held liable for a wrongful eviction. WEST HOLLYWOOD, CAL., MUN. CODE §§ 17.52.030, 17.08.010(11). Any person or entity violating any provisions of the Ordinance is liable for actual or statutory damages and punitive damages. WEST HOLLYWOOD, CAL., MUN. CODE § 17.68.010(d). Attorney fees are recoverable for a prevailing plaintiff. WEST HOLLYWOOD, CAL., MUN. CODE § 17.68.010(d).</p>
                          <h5>Santa Monica</h5>
                          <p>In Santa Monica, a landlord or any agent of the landlord can be held liable for a wrongful eviction. SANTA MONICA, CAL., MUN. CODE, §§ 1806(a), 1801(e). A landlord may be held liable for actual and punitive damages, including damages for emotional distress. SANTA MONICA, CAL., MUN. CODE, § 1806(f). Attorney fees are recoverable for a prevailing plaintiff. SANTA MONICA, CAL., MUN. CODE, § 1806(f).</p>
                          <h5>Beverly Hills</h5>
                          <p>In Beverly Hills, a landlord or any agent of the landlord can be liable for a wrongful eviction. BEVERLY HILLS, CAL., MUN. CODE, Title 4, ch. 5, §§ 501,703, 202. In a wrongful eviction action for a fraudulent owner- or relative-move-in eviction, a landlord is liable for triple the amount of the rent which would have been paid by the tenant had the tenant not been dispossessed, not exceeding six months. BEVERLY HILLS, CAL., MUN. CODE, Title 4, ch. 5, § 703(B). Attorney fees are recoverable for a prevailing plaintiff. BEVERLY HILLS, CAL., MUN. CODE, Title 4, ch. 5, § 703(B).</p>
                          <h5>Los Angeles</h5>
                          <p>In Los Angeles, a landlord or any agent of the landlord can be held liable for a wrongful eviction. LOS ANGELES, CAL., MUN. CODE, §§ 151.09(A), 151.02. Actual damages are automatically tripled where the landlord acts in bad faith in recovering possession of a rental unit for owner-, owner-relative, or resident-manager occupancy. LOS ANGELES, CAL., MUN. CODE § 151.30(I)(1). Attorney fees are recoverable for a prevailing plaintiff where the landlord acts in bad faith in recovering possession of a rental unit for owner-, owner-relative, or resident-manager occupancy. LOS ANGELES, CAL., MUN. CODE § 151.30(I)(1).</p>
