                          <h3>Northern California</h3>
                          <h5>Berkeley</h5>
                          <p>In Berkeley, a landlord or any agent of the landlord can be held liable for a wrongful eviction. BERKELEY, CAL., MUN. CODE §§ 13.76.130(A), 13.76.040(D). Actual damages may be tripled when a landlord’s conduct was willful. BERKELEY, CAL., MUN. CODE § 13.76.150(B). Emotional distress damages are tripled when there is a knowing violation or reckless disregard of a tenants’ rights. BERKELEY, CAL., MUN. CODE § 13.79.060(H)(3). Attorney fees are recoverable for a prevailing plaintiff. BERKELEY, CAL., MUN. CODE § 13.79.040(C).</p>
                          <h5>Oakland</h5>
                          <p>In Oakland, a landlord, their agent, or anyone assisting a landlord can be held liable for a wrongful eviction. OAKLAND, CAL., MUN. CODE §§ 8.22.370(A)(2), 8.22.340. Actual damages are automatically tripled. OAKLAND, CAL., MUN. CODE § 8.22.370(A)(2). Emotional distress damages are tripled when there is a knowing violation or reckless disregard of a tenant’s rights. OAKLAND, CAL., MUN. CODE § 8.22.370(A)(2). Attorney fees are recoverable for a prevailing plaintiff. OAKLAND, CAL., MUN. CODE § 8.22.370(A)(2).</p>
                          <h5>San Francisco</h5>
                          <p>In San Francisco, a landlord or any agent of the landlord can be held liable for a wrongful eviction. SAN FRANCISCO, CAL., ADMIN. CODE §§ 37.9(a),(f), 37.2(h). Actual damages are automatically tripled. SAN FRANCISCO, CAL., ADMIN. CODE § 37.9(f). Emotional distress damages are tripled when there is a knowing violation or reckless disregard of the tenants’ rights. SAN FRANCISCO, CAL., ADMIN. CODE § 37.9(f). Attorney fees are recoverable for a prevailing plaintiff. SAN FRANCISCO, CAL., ADMIN. CODE § 37.9(f).</p>
                          <h5>Richmond</h5>
                          <p>In Richmond, a landlord or any agent of the landlord can be held liable for a wrongful eviction. RICHMOND, CAL., MUN. CODE §§ 11.100.050(a),(g), 11.100.030(f). A landlord can be held liable for actual damages, including damages for emotional distress. RICHMOND, CAL., MUN. CODE § 11.100.050(g). Attorney fees are recoverable for a prevailing plaintiff. RICHMOND, CAL., MUN. CODE § 11.100.050(g).</p>
