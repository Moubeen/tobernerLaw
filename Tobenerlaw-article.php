                     <p>Eviction Protection Applies to Almost All Residential Units in San Francisco

                        Newly constructed buildings (i.e. buildings built after 1979) are no longer exempt from the just-cause for eviction protections under the San Francisco Rent Ordinance.

                        As of January 22, 2020, most residential units now have eviction protection under the Ordinance, including anti-harassment protection, regardless of the date of construction of the building or the number of units in the building. Units that previously had a substantial rehabilitation petition approved to exempt the property from the Ordinance also now have just-cause for eviction protections.

                        If you live in a building that has eviction protection, the landlord can only evict you if the landlord has a just-cause reason under the law.  Also, all eligible tenants that are evicted for a no-fault reason under the ordinance, such as an owner-move-in eviction or an Ellis Act eviction, are entitled to relocation benefits.  And, buyout procedures under the Ordinance apply to all units covered under the just-cause provisions.

                        Here are the 15 just causes:

                        (1) &nbsp; Failure to pay;
                        (2) &nbsp; Breach of a covenant in a lease;
                        (3) &nbsp; The tenant is creating a Nuisance;  See Landlord Nuisance
                        (4) &nbsp; The tenant is using the rental unit for any illegal purpose;
                        (5) &nbsp; The tenant, who had an oral or written agreement with the landlord which has terminated, has refused after written request or demand by the landlord to execute a written extension or renewal thereof for a further term of like duration and under such terms which are materially the same as in the previous agreement;
                        (6) &nbsp; The tenant has, after written notice, refused the landlord access to the rental unit;
                        (7) &nbsp; The tenant holding at the end of the term of the oral or written agreement is a subtenant not approved by the landlord;
                        (8) &nbsp; The owner seeks to move into the unit, or if the landlord is already living there or seeking to live there, the owner seeks to move a relative into a second unit;  See Owner Move-In Evictions
                        (9) &nbsp; The landlord seeks to recover possession in good faith in order to sell the unit in accordance with a condominium conversion;
                        (10) &nbsp; The landlord seeks to recover possession in good faith in order to demolish or to otherwise permanently remove the rental unit from housing use;
                        (11) &nbsp; The landlord seeks in good faith to remove temporarily the unit from housing use in order to be able to carry out capital improvements or rehabilitation work and has obtained all the necessary permits;  See Your Rights in a Capital Improvements Eviction
                        (12) &nbsp; The landlord seeks to recover possession in good faith in order to carry out substantial rehabilitation and has obtained all the necessary permits;
                        (13) &nbsp; The landlord wishes to withdraw from rent or lease all rental units within any detached physical structure;  See Your Rights in an Ellis Act Eviction
                        (14) &nbsp; The landlord seeks in good faith to temporarily recover possession of the unit solely for the purpose of effecting lead remediation;
                        (15) &nbsp; The landlord seeks to recover possession in good faith in order to demolish or to otherwise permanently remove the rental unit from housing use in accordance with the terms of a development agreement entered into by the City under Chapter 56 of the San Francisco Administrative Code.

                        A landlord who lives in the same unit with a tenant may evict without a just cause.

                        A master tenant cannot evict without just cause, unless the master tenant follows strict guidelines.

                        What if I have eviction control protection, but I do not have rent control protection under the San Francisco Rent Ordinance?

                        Due to the recent amendment of the San Francisco Rent Ordinance, almost all residential buildings in San Francisco have eviction protections, including anti-harassment protection, regardless of the date of construction of the building or the number of units in the building.  But, not all of these properties have rent-control protections that limit annual rent increases to a certain small percentage each year.

                        If you live in a building that was built after 1979 or you moved into a single-family home after January 1, 1996, you are not covered under the Ordinance’s rent caps.  If you do not have rent-ceiling protection under the San Francisco Rent Control Ordinance, you may have rent-ceiling protection under the new State of California Tenant Protection Act if your building is fifteen years or older and the unit meets all other requirements under the new state law.  Tenants should read our California Tenant Protection Act guide to determine if the rent control protections under the new state rent control law apply to their tenancy.

                        If you are a tenant with questions about the San Francisco Rent Ordinance or the new California Tenant Protection Act, call Tobener Ravenscroft LLP to speak with a tenant attorney about your rights.</p>
