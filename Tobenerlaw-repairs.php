                        <h3>Can I File A Lawsuit Against My Landlord For Failure To Make Repairs?</h3>
                        <p>Depending on the severity of the repair issues, a tenant can file a lawsuit against a landlord who fails to repair. Under California law, tenants may have one or more of the following causes of action against a landlord:

                        • Breach of Warranty of Habitability
                        • Breach of Quiet Enjoyment
                        • Negligent Infliction of Emotional Distress
                        • Intentional Infliction of Emotional Distress
                        • Wrongful Eviction
                        • Constructive Eviction
                        • Negligence and Negligence Per Se
                        • Violation of Local Rent Ordinance
                        • Unfair Business Practices
                        • Elder Abuse
                        • Harassment
                        • Uncured Building Inspection Violation

                        Some of the damages a tenant may seek to recover in a lawsuit against their landlord are rent that was previously paid to the landlord, out-of-pocket expenses, property damage, payment for emotional suffering, damages for physical harm, treble damages, punitive damages, and future damages. Also, the prevailing party to such a lawsuit may be entitled to attorney fees. CAL. CIV. CODE § 1942.4.
