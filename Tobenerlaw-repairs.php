                    <h2>Are California Landlords Responsible For Making Repairs To Their Rental Units?</h2>
                    <p>Yes. Landlords are required to make all necessary repairs to ensure a rental unit is habitable and to comply with state and local building and health codes. The Court has held that all residential rental agreements contain an “implied warranty of habitability”, which means regardless of what your lease says, your landlord has a duty to make sure the unit is livable, safe, and not in need of repair. Green v. Superior Court, 10 Cal. 3d 616 (1974).<br><br>
                    To be considered habitable, rental units must have effective waterproofing and weather protection; plumbing, gas, electricity, and electrical wiring and equipment in good working order; hot and cold water; adequate heat; all areas maintained free of garbage, rodents, and vermin; floors, stairways, and railings that are in good repair; and adequate garbage receptacles. CAL. CIV. CODE § 1941.1.<br><br>
                    The landlord is not responsible for damages caused by the tenant, their guest, family, or pets. CAL. CIV. CODE § 1941.2.</p>

